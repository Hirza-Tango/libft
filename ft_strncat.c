#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*c1;

	c1 = s1 + ft_strlen(s1);
	while (n-- && *s2)
		*c1++ = *s2++;
	*c1 = 0;
	return (s1);
}
